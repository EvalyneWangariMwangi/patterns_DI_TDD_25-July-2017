<?php

class Calculator{
	//addition function
	public function add($num1,$num2){
		return $num1 + $num2;
	}

	//substruction function
	public function subtract($num1,$num2){
		return $num1 - $num2;
	}

	//division
	public function divide($num1,$num2){
		return $num1/$num2;
	}

	//multiplication
	public function multiple($num1,$num2){
		return $num1*$num2;
	}

}

?>